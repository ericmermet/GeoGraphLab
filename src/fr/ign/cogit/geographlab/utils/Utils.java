	public static double [] inverseTableau (double [] t)
	{
        double [] rev;
        rev = new double [t.length];

        // On renverse le tableau
        for (int i=0; i<= t.length-1; i=i+1)
            rev[i] = t[t.length-1-i];

        return rev;
    }
	
